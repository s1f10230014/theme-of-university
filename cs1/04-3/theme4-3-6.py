def find_values(sequence, max_m):
    for m in range(2, max_m + 1):  
        for a in range(1, m):  
            for b in range(m):  
               
                if all((a * sequence[i] + b) % m == sequence[i + 1] for i in range(len(sequence) - 1)):
                    return a, b, m
    return None 

